// 1. Write a program using for loop that prints numbers from 1 to 10.

// For loop
console.log('For loop');
for (let x = 1; x <= 10; x++) console.log(x);

// While loop
console.log('While loop');
x = 1;
while (x <= 10) {
    console.log(x);
    x++;
}

//Do while loop
console.log('Do while loop');
x = 1;
do {
    console.log(x);
    x++;
}
while (x <= 10);
